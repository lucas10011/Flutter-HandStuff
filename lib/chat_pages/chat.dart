class Chat extends StatefulWidget {
  final String peerId;
  final String peerAvatar;
  final String currentUserId;

  Chat({Key key, @required this.peerId, @required this.peerAvatar,@required this.currentUserId}) : super(key: key);

  @override
  State createState() => new ChatState(peerId: peerId, peerAvatar: peerAvatar,currentUserId: currentUserId);
}

class ChatState extends State<Chat> {
  ChatState({Key key, @required this.peerId, @required this.peerAvatar,@required this.currentUserId});
  
  String currentUserId;
  String peerId;
  String peerAvatar;
  String peerName;


  String otherToken;
 
  var listMessage;
  String groupChatId;


  File imageFile;
  bool isLoading;
  bool isShowSticker;
  String imageUrl;

  List userDocument;

  final TextEditingController textEditingController = new TextEditingController();
  final ScrollController listScrollController = new ScrollController();
  final FocusNode focusNode = new FocusNode();

  @override
  void initState() {
    super.initState();
    focusNode.addListener(onFocusChange);

    groupChatId = '';
    print(peerAvatar);
    isLoading = false;
    isShowSticker = false;
    imageUrl = '';

    readLocal();
  }

  void onFocusChange() {
    if (focusNode.hasFocus) {
      // Hide sticker when keyboard appear
      setState(() {
        isShowSticker = false;
      });
    }
  }

  readLocal() async {


    setState(() {
     peerName = userDocument[1]; 
    });

    if (widget.currentUserId.hashCode <= peerId.hashCode) {
      groupChatId = '${widget.currentUserId}-$peerId';
    } else {
      groupChatId = '$peerId-${widget.currentUserId}';
    }

    final QuerySnapshot result = await Firestore.instance.collection('users').where('id', isEqualTo: '$peerId').getDocuments();
    final List<DocumentSnapshot> documents = result.documents;

    otherToken = documents[0]['token'];
    
    Firestore.instance.collection('users').document(widget.currentUserId).updateData({'chattingWith': peerId});
    Firestore.instance.collection('users').document(widget.currentUserId).collection('messages').document(groupChatId).updateData({'read':true,});
    print(otherToken);

    setState(() {});
  }

  Future getImage() async {
    imageFile = await ImagePicker.pickImage(source: ImageSource.gallery);

    if (imageFile != null) {
      setState(() {
        isLoading = false;
      });
      uploadFile();
    }
  }

  void getSticker() {
    // Hide keyboard when sticker appear
    focusNode.unfocus();
    setState(() {
      isShowSticker = !isShowSticker;
    });
  }

  Future uploadFile() async {
    String fileName = DateTime.now().millisecondsSinceEpoch.toString();
    firebase_storage.Reference reference = firebase_storage.FirebaseStorage.instance.ref().child('messages/$groupChatId/$fileName');
    firebase_storage.UploadTask uploadTask = reference.putFile(imageFile);
    firebase_storage.TaskSnapshot storageTaskSnapshot = await uploadTask.snapshot;
    storageTaskSnapshot.ref.getDownloadURL().then((downloadUrl) {
      imageUrl = downloadUrl;
      setState(() {
        isLoading = false;
        onSendMessage(imageUrl, 1);
      });
    }, onError: (err) {
      setState(() {
        isLoading = false;
      });
      Fluttertoast.showToast(msg: 'This file is not an image');
    });
  }

  void onSendMessage(String content, int type) {
    // type: 0 = text, 1 = image, 2 = sticker
    if (content.trim() != '') {
      textEditingController.clear();

      var documentReference = Firestore.instance
          .collection('messages')
          .document(groupChatId)
          .collection(groupChatId)
          .document(DateTime.now().millisecondsSinceEpoch.toString());

      Firestore.instance.runTransaction((transaction) async {
        await transaction.set(
          documentReference,
          {
            'idFrom': widget.currentUserId,
            'idTo': peerId,
            'timestamp': DateTime.now().millisecondsSinceEpoch.toString(),
            'content': content,
            'type': type
          },
        );
      });
      listScrollController.animateTo(0.0, duration: Duration(milliseconds: 300), curve: Curves.easeOut);
      DateTime timestamp = DateTime.now();
      Firestore.instance
          .collection('users')
          .document(widget.currentUserId)
          .collection('messages')
          .document(groupChatId).setData({
            'peerId':peerId,
            'groupChatId':groupChatId,
            'timestamp': timestamp,
            'lastmessage':content,
            'read': true,
            'notification':true,
          });
      Firestore.instance
          .collection('users')
          .document(peerId)
          .collection('messages')
          .document(groupChatId).setData({
            'peerId':widget.currentUserId,
            'groupChatId':groupChatId,
            'timestamp': timestamp,
            'lastmessage':content,
            'read':false,
            'notification':true,
          });


      

    } else {
      Fluttertoast.showToast(msg: 'Nothing to send');
    }
  }

  Widget buildItem(int index, DocumentSnapshot document) {
    if (document['idFrom'] == widget.currentUserId) {
      // Right (my message)
      return Row(
        children: <Widget>[
          document['type'] == 0
              // Text
              ? Container(
                  child: Text(
                    document['content'],
                    style: TextStyle(color: primaryColor),
                  ),
                  padding: EdgeInsets.fromLTRB(15.0, 10.0, 15.0, 10.0),
                  width: 200.0,
                  decoration: BoxDecoration(color: greyColor2, borderRadius: BorderRadius.circular(8.0)),
                  margin: EdgeInsets.only(bottom: isLastMessageRight(index) ? 20.0 : 10.0, right: 10.0),
                )
              : document['type'] == 1
                  // Image
                  ? Container(
                      child: FlatButton(
                        child: Material(
                          child: Image.network(document['content'],fit: BoxFit.cover,
                                              width: 200.0,
                                              height: 200.0,
                                              loadingBuilder:(BuildContext context, Widget child,ImageChunkEvent loadingProgress) {
                                              if (loadingProgress == null) return child;
                                                return Center(
                                                  child: CircularProgressIndicator(
                                                  value: loadingProgress.expectedTotalBytes != null ? 
                                                        loadingProgress.cumulativeBytesLoaded / loadingProgress.expectedTotalBytes
                                                        : null,
                                                  ),
                                                );
                                              },
                                            ),
                          borderRadius: BorderRadius.all(Radius.circular(8.0)),
                          clipBehavior: Clip.hardEdge,
                        ),
                        onPressed: () {
                          Navigator.push(
                              context, MaterialPageRoute(builder: (context) => FullPhoto(url: document['content'])));
                        },
                        padding: EdgeInsets.all(0),
                      ),
                      margin: EdgeInsets.only(bottom: isLastMessageRight(index) ? 20.0 : 10.0, right: 10.0),
                    )
                  // Sticker
                  : Container(
                      child: new Image.asset(
                        'images/${document['content']}.gif',
                        width: 100.0,
                        height: 100.0,
                        fit: BoxFit.cover,
                      ),
                      margin: EdgeInsets.only(bottom: isLastMessageRight(index) ? 20.0 : 10.0, right: 10.0),
                    ),
        ],
        mainAxisAlignment: MainAxisAlignment.end,
      );
    } else {
      // Left (peer message)
      return Container(
        child: Column(
          children: <Widget>[
            Row(
              children: <Widget>[
                isLastMessageLeft(index)
                    ? Material(
                        child: 
                        Image.network(userDocument[0],fit: BoxFit.cover,
                                              width: 35.0,
                                              height: 35.0,
                                              loadingBuilder:(BuildContext context, Widget child,ImageChunkEvent loadingProgress) {
                                              if (loadingProgress == null) return child;
                                                return Center(
                                                  child: CircularProgressIndicator(
                                                  value: loadingProgress.expectedTotalBytes != null ? 
                                                        loadingProgress.cumulativeBytesLoaded / loadingProgress.expectedTotalBytes
                                                        : null,
                                                  ),
                                                );
                                              },
                                            ),
                        borderRadius: BorderRadius.all(
                          Radius.circular(18.0),
                        ),
                        clipBehavior: Clip.hardEdge,
                      )
                    : Container(width: 35.0),
                document['type'] == 0
                    ? Container(
                        child: Text(
                          document['content'],
                          style: TextStyle(color: Colors.white),
                        ),
                        padding: EdgeInsets.fromLTRB(15.0, 10.0, 15.0, 10.0),
                        width: 200.0,
                        decoration: BoxDecoration(color: primaryColor, borderRadius: BorderRadius.circular(8.0)),
                        margin: EdgeInsets.only(left: 10.0),
                      )
                    : document['type'] == 1
                        ? Container(
                            child: FlatButton(
                              child: Material(
                                child:  Image.network(document['content'],fit: BoxFit.cover,
                                              width: 200.0,
                                              height: 200.0,
                                              loadingBuilder:(BuildContext context, Widget child,ImageChunkEvent loadingProgress) {
                                              if (loadingProgress == null) return child;
                                                return Center(
                                                  child: CircularProgressIndicator(
                                                  value: loadingProgress.expectedTotalBytes != null ? 
                                                        loadingProgress.cumulativeBytesLoaded / loadingProgress.expectedTotalBytes
                                                        : null,
                                                  ),
                                                );
                                              },
                                            ),
                                borderRadius: BorderRadius.all(Radius.circular(8.0)),
                                clipBehavior: Clip.hardEdge,
                              ),
                              onPressed: () {
                                Navigator.push(context,
                                    MaterialPageRoute(builder: (context) => FullPhoto(url: document['content'])));
                              },
                              padding: EdgeInsets.all(0),
                            ),
                            margin: EdgeInsets.only(left: 10.0),
                          )
                        : Container(
                            child: new Image.asset(
                              'images/${document['content']}.gif',
                              width: 100.0,
                              height: 100.0,
                              fit: BoxFit.cover,
                            ),
                            margin: EdgeInsets.only(bottom: isLastMessageRight(index) ? 20.0 : 10.0, right: 10.0),
                          ),
              ],
            ),

            // Time
            isLastMessageLeft(index)
                ? Container(
                    child: Text(
                      DateFormat('dd MMM kk:mm')
                          .format(DateTime.fromMillisecondsSinceEpoch(int.parse(document['timestamp']))),
                      style: TextStyle(color: greyColor, fontSize: 12.0, fontStyle: FontStyle.italic),
                    ),
                    margin: EdgeInsets.only(left: 50.0, top: 5.0, bottom: 5.0),
                  )
                : Container()
          ],
          crossAxisAlignment: CrossAxisAlignment.start,
        ),
        margin: EdgeInsets.only(bottom: 10.0),
      );
    }
  }

  bool isLastMessageLeft(int index) {
    if ((index > 0 && listMessage != null && listMessage[index - 1]['idFrom'] == widget.currentUserId) || index == 0) {
      return true;
    } else {
      return false;
    }
  }

  bool isLastMessageRight(int index) {
    if ((index > 0 && listMessage != null && listMessage[index - 1]['idFrom'] != widget.currentUserId) || index == 0) {
      return true;
    } else {
      return false;
    }
  }

  Future<bool> onBackPress() {
    if (isShowSticker) {
      setState(() {
        isShowSticker = false;
      });
    } else {
      Firestore.instance.collection('users').document(widget.currentUserId).updateData({'chattingWith': null});
      Navigator.pop(context);
    }

    return Future.value(false);
  }


